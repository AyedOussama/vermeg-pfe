package com.pfe2025.jobpostingservice.model.enums;



/**
 * Énumération des différents types de contrats proposés.
 */
public enum EmploymentType {
    /**
     * Contrat à durée indéterminée (temps plein)
     */
    FULL_TIME,

    /**
     * Contrat à durée indéterminée (temps partiel)
     */
    PART_TIME,

    /**
     * Contrat à durée déterminée
     */
    CONTRACT,

    /**
     * Poste temporaire
     */
    TEMPORARY,

    /**
     * Stage
     */
    INTERNSHIP,

    /**
     * Prestation indépendante
     */
    FREELANCE
}
